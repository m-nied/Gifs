   String res = new String("Blah ");
   ArrayList results = new ArrayList();
   String fin = null;
   int place=0, choice=1;
   public void getResult(String type){   
       place++;//increase finishing position each call
       if(place==1)//end appropriate suffix to position
          fin="st";
       else if(place==2)
          fin="nd";
       else if(place==3)
          fin="rd";
       else
          fin="th";
      res=type;//add type/racer to arraylist
      results.add("The "+res+" finished in "+place+fin+" place!\n");//add racer result to arraylist
      if(place==choice)//enables Results button after race is complete
         ResultsButton.setEnabled(true);
  }//wnd getResult()
