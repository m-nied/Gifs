    private void SelectRacerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SelectRacerButtonActionPerformed
            while((choice<2) ||(choice > 5)){//user input error checking
            try{
            String input = JOptionPane.showInputDialog("Please input the number of racer you would like in the race (2-5)");//asks for user input
            if(input==null)
                System.exit(0);
            choice = Integer.parseInt(input);//convert user input to string
            if((choice < 2)||(choice > 5))//checks and displays error message if user input is invalid
                JOptionPane.showMessageDialog(rootPane, "Please enter a number between '2' and '5'");
            }
            catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(rootPane, "Please enter a number between '2' and '5'");
            }
        }//end while
        SelectRacerButton.setEnabled(false);//disable selectRacerButton after valid user input
        racer[0] = new Racer("bird.gif", 1, this, panel1);//declare new Racers 
        racer[1] = new Racer("ram.gif", 2, this,panel2); 
        racer[2] = new Racer("bunny.gif", 3, this,panel3); 
        racer[3] = new Racer("dino.gif", 4, this,panel4); 
        racer[4] = new Racer("horse.gif", 5, this,panel5);
        
        for(int i=0;i<choice;i++){//start threeads based on user input
            racer[i].start();
        }//end for
        
    }//GEN-LAST:event_SelectRacerButtonActionPerformed

